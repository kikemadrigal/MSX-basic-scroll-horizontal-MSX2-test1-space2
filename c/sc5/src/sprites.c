/**************DEFINICIONES************/
void cargar_sprites_en_VRAM(){

    // lo metemos en la tabla de dEfinición de patrones
    // el 1 argumento es la posición en la poisión en la tabla de definición de sprites (sprite pattern),tenemos que ir sumando 4 porque fusion c es asi
    // el 2 argumentos son los datos del sprite
    // el 3 argumento es el numero de bytes del patron
    SetSpritePattern( 0, sprite_nave_derecha1, 32);
    SetSpritePattern( 4, sprite_nave_derecha2, 32);
    SetSpritePattern( 4*2, sprite_nave_arriba1, 32);
    SetSpritePattern( 4*3, sprite_nave_arriba2, 32);
    SetSpritePattern( 4*4, sprite_nave_abajo1, 32);
    SetSpritePattern( 4*5, sprite_nave_abajo2, 32);

    //Le ponemos el color al sprite
    //SC5SpriteColors(pplano, color_sprite_nave_derecha1);
    //SC5SpriteColors(pplano+1, color_sprite_nave_derecha2);
    SC5SpriteColors(0, color_sprite_nave_derecha1);
    SC5SpriteColors(2*1, color_sprite_nave_derecha2);
    // Sobre PutSprite, si queremos podemos hacer que se vea ya el sprite del personaje con
    // 1 El plano o su definición en la tabla de atributos, es donde alamcenará la posición x e y, y su sprite
    // 2 la definición en sprite pattern que va de 4 en 4
    // 3 posición eje x
    // 4 posición eje y
    // 5 color  





    //Vamos a meter en la VRAM el sprite de los enemigos
    //El 7 es el tanque
    SetSpritePattern( 4*6, sprite_enemigo1, 32);
    //El 8 es la nave enemiga mirando hacia la izquierda
    SetSpritePattern( 4*7, sprite_enemigo2_izquierda, 32);
    //El 9 es la ave enemiga mirando a la derecha
    SetSpritePattern( 4*8, sprite_enemigo2_derecha, 32);
    // No vamos a definirles el color lo haremos sobre la marcha
    SC5SpriteColors(2*2, color_sprite_enemigo1);
    SC5SpriteColors(2*3, color_sprite_enemigo2_izquierda);
    SC5SpriteColors(2*4, color_sprite_enemigo2_derecha);


    SetSpritePattern( 4*9, sprite_bloque_negro, 32);
    SC5SpriteColors(9, color_sprite_bloque_vacio);
    //El sprite del fuego es el 6, vamos a metermlo en la VRAM
    SetSpritePattern( 4*10, sprite_disparo, 32);
    //SC5SpriteColors(fire_prueba.plano, color_sprite_disparo);
}









